    <h2>Gunakan menu disebelah kiri Anda untuk:</h2>
    <h4>1. Master Mahasiswa</h4>
    <p>Halaman ini menampilkan daftar mahasiswa dalam bentuk tabel. Jika diklik link EDIT maka
        akan ditampilkan halaman edit mahasiswa, jika diklik link DELETE maka akan menghapus data
        mahasiswa. Di halaman ini juga terdapat link ke halaman tambah data mahasiswa dan jika
        diklik akan ditampilkan form untuk menambah mahasiswa baru. </p>
    <h4>2. Master Masterkuliah</h4>
    <p>Halaman ini menampilkan daftar matakuliah dalam bentuk tabel. Jika diklik link EDIT maka
        akan ditampilkan halaman edit matakuliah, jika diklik link DELETE maka akan menghapus data
        matakuliah. Di halaman ini juga terdapat link ke halaman tambah data matakuliah dan jika diklik
        akan ditampilkan form untuk menambah matakuliah baru. </p>
    <h4>3. Entry Nilai</h4>
    <p>Halaman Entri Nilai
        Halaman ini menampilkan daftar nilai mahasiswa dalam bentuk tabel. Jika diklik link EDIT maka
        akan ditampilkan halaman edit nilai, jika diklik link DELETE maka akan menghapus data nilai.
        Di halaman ini juga terdapat link ke halaman entri nilai baru dan jika diklik akan ditampilkan
        form untuk mengentri nilai. Di dalam daftar nilai mahasiswa, juga terdapat filter data
        berdasarkan Nama Mahasiswa dan Nama Matakuliah.</p>
    <h4>4. About Us</h4>
    <p>Halaman About Us
        Halaman ini menampilkan data pribadibeserta foto. Data yang disajikan hendaknya
        merupakan data yang sebenarnya (bukan fiktif).</p>
